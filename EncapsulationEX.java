package oops;

public class EncapsulationEX {
	
	private int Acc;
	
	public int getAcc() {
		return Acc;
	}
	public void setAcc(int acc) {
		Acc = acc;
	}
	
	
	public String getNAme() {
		return NAme;
	}
	public void setNAme(String nAme) {
		NAme = nAme;
	}
	
	
	public String getPlace() {
		return place;
	}
	public void setPlace(String place) {
		this.place = place;
	}
	
	
	private String NAme;
	private String place;
	

}
